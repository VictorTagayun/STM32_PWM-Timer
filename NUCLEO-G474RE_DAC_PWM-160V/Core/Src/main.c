  {
	  HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
	  HAL_Delay(50);
		if (duty_cycle == 0)
		{
			duty_cycle_increase = 1; //duty_cycle + 10; // div by 10; // div by 10
			HAL_Delay(4000);
		}
		if (duty_cycle == 1000)
		{
			duty_cycle_increase = 0;
			HAL_Delay(4000);
		}
		if (duty_cycle_increase)
			duty_cycle = duty_cycle + 1;
		else
			duty_cycle = duty_cycle - 1;
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
